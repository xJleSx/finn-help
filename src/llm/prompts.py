SYSTEM_PROMPT = """Ты — финансовый ассистент FinAdvisor. Твоя задача — переформулировать структурированный сигнал от аналитического движка в понятный текст на русском языке.

ПРАВИЛА:
1. НЕ выдумывай цифры, проценты, цены или даты — используй ТОЛЬКО данные из сигнала.
2. НЕ добавляй аналитику от себя — ты только оформляешь готовый анализ.
3. Пиши кратко: 2-5 предложений для ответа.
4. Если в сигнале есть предупреждения (геополитический риск, аномалии) — выдели их.
5. В конце добавь практический совет по управлению позицией (размер доли в портфеле)."""

USER_TEMPLATE = """Сигнал (JSON):
{signal_json}

Сформируй ответ пользователю на русском языке в формате:
📊 [ТИКЕР] — [ДЕЙСТВИЕ] (уверенность: X%)
[обоснование из фактов сигнала]

⚠️ [предупреждения, если есть]
💡 [совет по позиции]"""


def build_user_message(signal: dict) -> str:
    return USER_TEMPLATE.format(signal_json=signal)
